    private int _selectedCat = 0;

    public EmojiPickerDialog()
    {
        InitializeComponent();
    }

    private void Window_Loaded(object sender, RoutedEventArgs e)
    {
        string[] catIcons = ["😀","🐱","🍎","⚽","🌍","❤"];

        for (int i = 0; i < _categories.Length; i++)
        {
            int idx = i;
            string catKey  = _categories[i].KeyPrefix;
            string catName = Application.Current.TryFindResource(catKey) as string ?? catKey;
            string icon    = catIcons[i];

            var btn = new Button
            {
                Content  = $"{icon}",
                FontSize = 20,
                Width    = 48,
                Height   = 40,
                Margin   = new Thickness(2, 0, 2, 0),
                ToolTip  = catName,
                Tag      = idx,
            };
            btn.Click += (_, _) => { _selectedCat = idx; LoadCategory(idx); };
            CatBar.Children.Add(btn);
        }

        LoadCategory(0);
    }

    private void LoadCategory(int catIdx)
    {
        EmojiPanel.Children.Clear();
        var emojis = _categories[catIdx].Emoji;

        foreach (var emoji in emojis)
        {
            string em = emoji;
            var btn = new Button
            {
                Content    = em,
                FontSize   = 22,
                Width      = 44,
                Height     = 44,
                Margin     = new Thickness(1),
                FontFamily = new FontFamily("Segoe UI Emoji"),
                Background = System.Windows.Media.Brushes.Transparent,
                BorderThickness = new Thickness(1),
            };
            btn.Click += (_, _) => EmojiInserted?.Invoke(em);
            EmojiPanel.Children.Add(btn);
        }
    }

    private void OnClose(object sender, RoutedEventArgs e) => Close();
